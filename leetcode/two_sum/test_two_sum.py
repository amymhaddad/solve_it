from two_sum import (
    indices_approach_1,
    indices_approach_2,
    indices_approach_3,
    indices_approach_4,
    indices_approach_5,
)


def test_two_sum():
    assert indices_approach_1([2, 7, 11, 15], 9) == [0, 1]
    assert indices_approach_2([2, 7, 11, 15], 9) == [0, 1]
    assert indices_approach_3([2, 7, 11, 15], 9) == [0, 1]
    assert indices_approach_4([2, 7, 11, 15], 9) == [0, 1]
    assert indices_approach_5([2, 7, 11, 15], 9) == [0, 1]
